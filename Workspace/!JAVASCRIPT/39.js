/* 반복문을 이용한 합계 구하기(1 ~ 100) */

'use strict';

{
    let sum = 0;

    for (let n = 1; n < 101; n++) {
        sum += n;
    }
    
    console.log(sum);
}

/* ==================================================================================================== */

{
    let sum = 0;
    let n = 0;
    
    while(n < 100) {
        n++;
        sum += n;
    }
    
    console.log(sum);
}

/* ==================================================================================================== */

{
    let sum = 0;
    let n = 1;
    
    do {
        sum += n;
        n++;
    } while (n < 101);
    
    console.log(sum);
}

