'use strict';

let n1, n2, n;
let tot = 0;

n1 = +prompt('작은 수: ');
n2 = +prompt('큰 수: ');

n = n1;

do {
    tot += n;
    n++;

} while (n <= n2);

alert(`${n1}부터 ${n2}까지의 합: ${tot}`);

