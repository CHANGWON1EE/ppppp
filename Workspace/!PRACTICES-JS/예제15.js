'use strict';

let n1, n2;
let tot = 0;

for (; ;) {
    n1 = +prompt('작은 수: ');
    n2 = +prompt('큰 수: ');

    if (n1 <= n2) {

        for (let n = n1; n <= n2; n++) {

            tot += n;

        }
        
        alert(`${n1}부터 ${n2}의 합: ${tot}`);
        
        break;

    }   

}

